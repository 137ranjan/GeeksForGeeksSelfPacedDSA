import java.util.ArrayList;

/**
 * DyanamicArray
 */
public class DyanamicArray {

    public static void main(String[] args) {
        ArrayList<Integer> arrList = new ArrayList<>();
        arrList.add(1);
        arrList.add(2);
        arrList.add(3);
        for (Integer i : arrList) {
            System.out.print(i + ", ");
        }
        System.out.println("\n");

    }
}